#include "../header/push_swap.h"

/**
 * @brief Convertit une chaîne de caractères représentant un entier 
 *        en un entier long. Ignore les espaces initiaux et gère 
 *        un signe de début (+ ou -).
 * 
 * @param str Chaîne de caractères contenant le nombre à convertir.
 * @return La valeur convertie en type long.
 */
long	ft_atoi(const char *str)
{
	long	i;
	long	number;
	int		sign;

	i = 0;
	number = 0;
	sign = 1;
	while (str[i] && (str[i] == 32 || (str[i] >= 9 && str[i] <= 13)))
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		number = (number * 10) + (str[i] - '0');
		i++;
	}
	return (number * sign);
}
